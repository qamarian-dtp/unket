// Package unket implements the unket (Unique Number basKET) data type.
//
// But what is an unket?
//
// Well, an unket is an abstract basket. It contains unique whole numbers
// (1 <= x <= infinity), and numbers can be picked out of the basket. Number picking are
// done randomly.
//
// But of what use if this?
//
// Unkets are used whenever deemed appropriate, but one common example is: when you want to
// randomly pick an element of an array.
